        "https://picsum.photos/seed/${id}a/800/600",
        "https://picsum.photos/seed/${id}b/800/600"
    ),
    descriptionEs = descriptionEs, descriptionEn = descriptionEn,
    openingHours = openingHours,
    menuPrice = menuPrice, currency = "EUR", todayHasMenu = true, servesLunch = true,
    cuisineEmoji = cuisineEmoji, cuisineType = cuisineType,
    rating = rating, userRatingCount = userRatingCount,
    editorialSummary = editorialSummary, aiSummary = aiSummary, reviews = reviews,
    servesVegetarianFood = servesVegetarianFood, servesGlutenFree = servesGlutenFree,
    outdoorSeating = outdoorSeating, reservable = reservable,

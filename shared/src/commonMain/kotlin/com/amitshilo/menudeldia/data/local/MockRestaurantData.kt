import kotlinx.datetime.DayOfWeek
import kotlinx.datetime.LocalTime

private fun lunchSlot(day: DayOfWeek) =
    OpeningHours(dayOfWeek = day, openTime = LocalTime(13, 0), closeTime = LocalTime(16, 0))

private val weekdayLunch = (1..5).map { lunchSlot(DayOfWeek(it)) }
private val weekdayAndSatLunch = (1..6).map { lunchSlot(DayOfWeek(it)) }
private val sevenDayLunch = (1..7).map { lunchSlot(DayOfWeek(it)) }

private fun review(author: String, rating: Int, text: String) = Review(
    authorName = author, rating = rating, text = text,
    authorPhotoUri = null, originalText = null, relativeTime = "Hace 2 semanas",
)

private val defaultIncludes = listOf("Pan", "Bebida", "Postre o café")
private val fullIncludes = listOf("Pan", "Bebida", "Postre", "Café")

